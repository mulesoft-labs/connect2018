package com.mulesoft.social;

import java.util.ArrayList;

public class Choices {
	ArrayList<String> artPieces = new ArrayList();
	ArrayList<String> paintColors = new ArrayList();
	
	public ArrayList<String> getArtPieces() {
		return artPieces;
	}
	public void setArtPieces(ArrayList<String> artPieces) {
		this.artPieces = artPieces;
	}
	public ArrayList<String> getPaintColors() {
		return paintColors;
	}
	public void setPaintColors(ArrayList<String> paintColors) {
		this.paintColors = paintColors;
	}
	
	
}
